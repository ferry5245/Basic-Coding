print('Masukkan 5 Bilangan untuk Dihitung Rata-ratanya\n')
A=eval(input('Bilangan Pertama : '))
B=eval(input('Bilangan Kedua   : '))
C=eval(input('Bilangan Ketiga  : '))
D=eval(input('Bilangan Keempat : '))
E=eval(input('Bilangan Kelima  : '))
mean=(A+B+C+D+E)/5
print('Bilangan yang tercantum %d, %d, %d, %d, dan %d'%(A,B,C,D,E))
print('Rata-rata Bilangan : %.3f'%(mean))
